/**
 * @brief   Set SPI1 communication baud rate
 * @param   speed: SPI1 baud rate prescaler
 * @arg     SPI_BAUDRATEPRESCALER_2:   Prescaler 2
 * @arg     SPI_BAUDRATEPRESCALER_4:   Prescaler 4
 * @arg     SPI_BAUDRATEPRESCALER_8:   Prescaler 8
 * @arg     SPI_BAUDRATEPRESCALER_16:  Prescaler 16
 * @arg     SPI_BAUDRATEPRESCALER_32:  Prescaler 32
 * @arg     SPI_BAUDRATEPRESCALER_64:  Prescaler 64
 * @arg     SPI_BAUDRATEPRESCALER_128: Prescaler 128
 * @arg     SPI_BAUDRATEPRESCALER_256: Prescaler 256
 * @retval  None
 */
void spi1_set_speed(uint32_t speed)
{
    __HAL_SPI_DISABLE(&hspi1);
    hspi1.Instance->CR1 &= ~SPI_CR1_BR_Msk;
    hspi1.Instance->CR1 |= speed;
    __HAL_SPI_ENABLE(&hspi1);
}

/**
 * @brief   SPI1 read-write one byte of data
 * @param   txdata: One byte of data to be written
 * @retval  One byte of data read
 */
uint8_t spi1_read_write_byte(uint8_t txdata)
{
    uint8_t rxdata;

    if (HAL_SPI_TransmitReceive(&hspi1, &txdata, &rxdata, 1, 1000) != HAL_OK)
    {
        return 0;
    }

    return rxdata;
}

