void dcmi_dma_init(uint32_t mem0addr, uint32_t mem1addr, uint16_t memsize)
{
    if (mem1addr == 0)
    {
        HAL_DMA_Start(&hdma_dcmi, (uint32_t)&DCMI->DR, mem0addr, memsize);
    }
    else
    {
        HAL_DMAEx_MultiBufferStart(&hdma_dcmi, (uint32_t)&DCMI->DR, mem0addr, mem1addr, memsize);
        __HAL_DMA_ENABLE_IT(&hdma_dcmi, DMA_IT_TC);
    }
}

/**
 * @brief  starts the DCMI transfer
 * @param  None
 * @retval None
 */
void dcmi_start(void)
{
    lcd_set_cursor(0, 0);                   /* Set the coordinates to the origin */
    lcd_write_ram_prepare();                /* Start writing GRAM */
    __HAL_DMA_ENABLE(&hdma_dcmi);           /* Enable DMA */
    DCMI->CR |= DCMI_CR_CAPTURE;            /* DCMI captures the enable */
}

/**
 * @brief   stops DCMI transmission
 * @param   None
 * @retval  None
 */
void dcmi_stop(void)
{
    DCMI->CR &= ~(DCMI_CR_CAPTURE);         /* DCMI capture is closed */
    while (DCMI->CR & 0X01);                /* Wait for the transfer to end */
    __HAL_DMA_DISABLE(&hdma_dcmi);          /* Turn off DMA */
}

